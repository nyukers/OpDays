prompt ------------------------------------------

prompt _ Size of SEC_AUDIT + SEC_JOURNAL (sizeaudit.sql)

prompt ------------------------------------------

col segment_name format a20

select segment_name, (sum(bytes)/1024) as Size_in_Kb 
from dba_extents
where 
 segment_name = 'SEC_AUDIT'
 and
 owner='BARS'
 group by segment_name;

select segment_name, (sum(bytes)/1024) as Size_in_Kb 
from dba_extents
where 
  segment_name = 'SEC_JOURNAL'
 and
  owner='BARS'   
 group by segment_name;

