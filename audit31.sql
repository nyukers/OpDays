-- hostname which not in Audit 
prompt ------------------------------------------

prompt _ Перелiк станцiй яких незафiксовано в журналi АБС  (audit31.sql)
prompt  за останнiй перiод (вони HE працювали в ОДБ останню добу)
prompt ------------------------------------------

col hostname format a20

select * from  host_ip_adres  
where 
hostname not in 
 (select machine from  sec_audit
  where
  rec_date 
BETWEEN (TO_DATE('&1 00:00:00', 'dd/mm/yyyy HH24:MI:SS')-&2)
AND     (TO_DATE('&1 23:59:59', 'dd/mm/yyyy HH24:MI:SS')-&3))
order by ip;

