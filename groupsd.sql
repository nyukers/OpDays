col FIO format a30
col NAME format a40

select s.id, s.fio, s.tabn, gr.name, gs.SECG as С_Дб_Кр
from staff s, groups gr, groups_staff gs
	 where (s.id=gs.idu) and (gr.id=gs.idg)
order by s.fio;
