col FIO format a25

select st.TABN, st.fio, 
 TO_CHAR(MAX(sa.rec_date), 'dd/mm/yyyy HH24:MI:SS') as Finish
from sec_audit sa, staff st
WHERE 
 sa.REC_UID = st.ID
AND sa.rec_date 
BETWEEN (TO_DATE('&1 12:55:00', 'dd/mm/yyyy HH24:MI:SS')-&2)
AND     (TO_DATE('&1 14:00:00', 'dd/mm/yyyy HH24:MI:SS')-&2)
group by st.fio, st.TABN
order by Finish;
